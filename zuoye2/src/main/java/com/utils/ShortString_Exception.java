package com.utils;

public class ShortString_Exception extends Exception {

    public ShortString_Exception() {
        super();
    }

    public ShortString_Exception(String message) {
        super(message);
    }

    public ShortString_Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public ShortString_Exception(Throwable cause) {
        super(cause);
    }

}
